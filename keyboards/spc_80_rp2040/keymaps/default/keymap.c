bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        switch (keycode) {
            case BL_TOGG: // Управляем LED1
                // Просто инверсия состояния без хранения
                if (readPin(GP25)) {
                    writePinLow(GP25);
                } else {
                    writePinHigh(GP25);
                }
                return false;

            case BL_UP: // Управляем LED2
                if (readPin(GP24)) {
                    writePinLow(GP24);
                } else {
                    writePinHigh(GP24);
                }
                return false;

            case BL_DOWN: // Управляем LED3
                if (readPin(GP23)) {
                    writePinLow(GP23);
                } else {
                    writePinHigh(GP23);
                }
                return false;
        }
    }
    return true;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    // Сначала всё погасим
    writePinHigh(GP26);
    writePinHigh(GP27);

    switch (get_highest_layer(state)) {
        case 1: // FN1
            writePinLow(GP27);
            break;
        case 2: // FN2
            writePinLow(GP26);
            break;
        case 3: // FN3
            writePinLow(GP26);
            writePinLow(GP27);
            break;
        default: // Base layer
            // оба погашены
            break;
    }

    return state;
}


